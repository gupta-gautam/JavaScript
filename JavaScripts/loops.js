// Loops

// For Loops
for(let i=1;i<=10;i++){
console.log(i);
}

console.log("loop has ended");

// Calculate sum of 1 to 5
let sum = 0;
for(let i = 1; i <= 5; i++ ){
sum += i;
}
console.log(sum);


// While Loops
let i =1;
while(i<=5){
console.log("i=",i);
i++;
}

// Do-While Loops

let j = 1;
do{
console.log("j=",j);
j++;
} while(j<=8);